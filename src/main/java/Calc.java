public class Calc {

    public static int sum(int a, int b) {
        return a + b;
    }

    public static int subtract(int a, int b){
        return a - b;
    }

    public static int mult(int a, int b){
        return a * b;
    }

    public static int dev(int a, int b){
        return a / b;
    }
}
